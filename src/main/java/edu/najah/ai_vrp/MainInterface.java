    int oldTemp = 0;
    private void updateTempContinously(JLabel label){
        while (true) {
            SimulatedAnnealing<Shipment> SA = SimulatedAnnealing.<Shipment>getInstance();
            try{
                Thread.sleep(10);
            }catch(Exception ex){
                ex.printStackTrace();
            }
            if(SA == null)
                continue;
            
            if(oldTemp == SA.currentTemp)
                continue;
            SwingUtilities.invokeLater(new Runnable() {
                public void run() { 
                
                    label.setText(Integer.toString(oldTemp));
                    oldTemp = SA.currentTemp;
                    
                }
            });
        }
    }
    
    private void updateMapContinously() {
        while (true) {
            SimulatedAnnealing<Shipment> SA = SimulatedAnnealing.<Shipment>getInstance();
            try{
                Thread.sleep(10);
            }catch(Exception ex){
                ex.printStackTrace();
            }
            if(SA == null || !SA.repaint)
                continue;
            SA.repaint = false;
            // Use SwingUtilities.invokeLater to update the JLabel on the EDT
            SwingUtilities.invokeLater(new Runnable() {
                public void run() {
                    drawPath();
                    
                }
            });
        }
    }
    
    private void showShipmentState(Shipment shipment){
        JTextArea textArea = new JTextArea(20, 50); // 20 rows and 50 columns
        textArea.setText(shipment.toString());
        textArea.setLineWrap(true);
        textArea.setWrapStyleWord(true);
        textArea.setEditable(false); // Make it read-only

        // Wrap the JTextArea in a JScrollPane
        JScrollPane scrollPane = new JScrollPane(textArea);
        scrollPane.setVerticalScrollBarPolicy(JScrollPane.VERTICAL_SCROLLBAR_ALWAYS);
        scrollPane.setHorizontalScrollBarPolicy(JScrollPane.HORIZONTAL_SCROLLBAR_AS_NEEDED);

        // Show a JOptionPane with the JTextArea inside a JScrollPane
        JOptionPane.showMessageDialog(null, scrollPane, "Long Message", JOptionPane.INFORMATION_MESSAGE);
   
    }
    
