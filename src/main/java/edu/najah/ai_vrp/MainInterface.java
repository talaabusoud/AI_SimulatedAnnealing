    private void board1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_board1MouseClicked
        // TODO add your handling code here:
        Graphics g = board1.getGraphics();
        Node node = new Node(evt.getX(), evt.getY());
        node.id = currentId;
        if(!this.startNodeSet){
            node.isStart = true;
            this.startNodeSet = true;
        }else{
            node.readCapacity();
        }
        currentId++;
        nodesList.add(node);
        jLabel4.setText(Integer.toString(nodesList.size()));
        this.drawNode(node, g);
    }//GEN-LAST:event_board1MouseClicked


