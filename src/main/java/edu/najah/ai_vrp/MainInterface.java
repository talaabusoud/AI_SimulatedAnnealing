    private void drawPath(){
        Graphics g = board1.getGraphics();
        board1.paint(g);
        for(Node temp:nodesList){
            this.drawNode(temp, g);
        }
        for(ArrayList<Node> paths: this.trucksManager.getDisplayPath()){
            Color randomColor = this.getRandomColor();
            g.setColor(randomColor);
            for(int i = 0; i < paths.size() - 1; i++){
                Node current = paths.get(i);
                Node next = paths.get(i+1);
                g.drawLine(current.x, current.y, next.x, next.y);
            }
        }
        Color purple = new Color(255,204,204);
        
        g.setColor(purple);
        Font verdanaBold = new Font("Verdana", Font.BOLD, 12);
        g.setFont(verdanaBold);
        g.drawString("Current Distance :"+this.trucksManager.calculateCurrentDistance(), 860, 610);
        SimulatedAnnealing<Shipment> SA = SimulatedAnnealing.<Shipment>getInstance();
        g.drawString("Current Temprature :"+ (SA == null || SA.currentTemp == 0 ? "Not started" : SA.currentTemp.toString()), 860, 635);
    }
    
    private Color getRandomColor(){
        Random rand = new Random();
        float h = rand.nextFloat();
        float s = rand.nextFloat() * 0.5f;
        float b = 0.5f + rand.nextFloat() * 0.5f;
        return Color.getHSBColor(h, s, b);
    }
    
    private void drawNode(Node temp, Graphics g){
        if(temp.isStart){
            g.setColor(this.startNodeColor);
        }else{
            g.setColor(this.normalColor);
            g.drawString(Integer.toString(temp.capacity),temp.x-radius/2, temp.y + 2*radius);
        }
        g.drawString(Integer.toString(temp.id),temp.x-radius/2, temp.y - radius);
        g.drawOval(temp.x-radius/2,temp.y-radius/2, radius, radius);
        g.fillOval(temp.x-radius/2,temp.y-radius/2, radius, radius);
    }
